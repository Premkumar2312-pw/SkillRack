#include <stdio.h>
#include <string.h>

int main() {
    char *d[6][3] = {
        {"- - -", "- * -", "- - -"},   // 1
        {"* - -", "- - -", "- - *"},   // 2
        {"* - -", "- * -", "- - *"},   // 3
        {"* - *", "- - -", "* - *"},   // 4
        {"* - *", "- * -", "* - *"},   // 5
        {"* - *", "* - *", "* - *"}    // 6
    };

    char s[101];
    scanf("%s", s);

    for (int i = 0; s[i]; i++) {
        int n = s[i] - '1';
        for (int j = 0; j < 3; j++) {
            printf("%s\n", d[n][j]);
        }
    }
    return 0;
}